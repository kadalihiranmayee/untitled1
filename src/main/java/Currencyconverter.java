public class Currencyconverter {
    public Currencyconverter(){
    }
    public int sek_to_rupees(int sek){
        int return_value1 = sek*9;
        return return_value1;
    }
    public int dollar_to_rupees(int dollar){
        int return_value2 = dollar*70;
        return return_value2;
    }
    public int pounds_to_rupees(int pound){
        int return_value3 = pound*100;
        return return_value3;
    }
    public int euros_to_rupees(int euro){
        int return_value4 = euro*85;
        return return_value4;
    }
}
